# Binary Search Summary
# Definition:
# Binary search is an efficient algorithm to find a target value in a sorted array or list. It works using divide and conquer: repeatedly dividing the search space in half.

# Key Requirements
# Sorted Data:
# The array must be sorted in ascending or descending order. If not, binary search will fail.
# Constant-Time Indexing:
# You must be able to access elements by index in O(1) time, e.g., arrays or lists.

# Complexity
# Time Complexity: O(logn)
# Space Complexity: O(n)

